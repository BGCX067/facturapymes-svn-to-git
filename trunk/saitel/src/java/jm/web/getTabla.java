package jm.web;

import java.io.*;

import javax.servlet.*;
import javax.servlet.http.*;

/**
 * Servlet que consigue los datos de una tabla en formato JSON.
 */
public class getTabla extends HttpServlet {
   
    private String _ip = null;
    private int _puerto = 1433;
    private String _db = null;
    
    public void init(ServletConfig config) throws ServletException
    {
        this._ip = config.getServletContext().getInitParameter("_IP");
        this._puerto = Integer.parseInt(config.getServletContext().getInitParameter("_PUERTO"));
        this._db = config.getServletContext().getInitParameter("_DB");
    }
   
/**
 * Funci�n que retorna al cliente los datos de una tabla filtrada en formato JSON.
 * @param request servlet request
 * @param response servlet response
 * @return una cadena de respuesta al cliente.
*/     
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession sesion = request.getSession(true);
        String usuario = (String)sesion.getAttribute("usuario");
        String clave = (String)sesion.getAttribute("clave");
        
        response.setContentType("text/html;charset=UTF-8");
        response.setHeader("Pragma", "no-cache"); 
        response.setHeader("Expires", "Mon, 01 Jan 2001 00:00:01 GMT"); 
        response.setHeader("Cache-Control", "no-store"); 
        response.setHeader("Cache-Control", "must-revalidate"); 
        response.setHeader("Cache-Control", "no-cache");
        PrintWriter out = response.getWriter();
        String t = request.getParameter("t");
        String c = request.getParameter("c");    
        String w = request.getParameter("w");
        String p = request.getParameter("p") != null ? request.getParameter("p") : "0";
        DataBase db = new DataBase(this._ip, this._puerto, this._db, usuario, clave);
        try {
            out.print(db.paginar(t, c, w, Integer.parseInt(p), 100));
        } finally { 
            db.cerrar();
            out.close();
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
    * Handles the HTTP <code>GET</code> method.
    * @param request servlet request
    * @param response servlet response
    */
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    } 

    /** 
    * Handles the HTTP <code>POST</code> method.
    * @param request servlet request
    * @param response servlet response
    */
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }

    /** 
    * Returns a short description of the servlet.
    */
    public String getServletInfo() {
        return "Short description";
    }
    // </editor-fold>
}
